// scripts/ces-citation-audit.mjs
// Citation integrity audit (deterministic, no model). Parses every lesson's "## Sources",
// cross-checks each DOI against the shared open-access corpus, and Crossref-verifies any
// DOI not in the corpus. Produces the source artifacts in docs/ces-curriculum/teacher/:
//   master-source-usage-map.md, sources-needed.md, fake-claims-audit.md
// Run: node scripts/ces-citation-audit.mjs

import fs from 'fs';
import path from 'path';

const LESSONS_DIR = 'docs/ces-curriculum/academy-import/_lessons';
const TEACHER = 'docs/ces-curriculum/teacher';
const CORPUS = 'docs/CentenarianAcademy/shared-sources/bibliography.json';
const MAILTO = 'tools@awews.com';
fs.mkdirSync(TEACHER, { recursive: true });

const corpus = JSON.parse(fs.readFileSync(CORPUS, 'utf8'));
const corpusDoi = new Set(corpus.map((s) => (s.doi || '').toLowerCase().trim()).filter(Boolean));

const doiRe = /\b10\.\d{4,9}\/[^\s)"']+/i;
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// parse Sources per lesson
const sources = new Map(); // key (doi or normalized text) -> { text, doi, lessons:Set }
const noDoi = [];
for (const f of fs.readdirSync(LESSONS_DIR).filter((x) => x.endsWith('.md'))) {
  const md = fs.readFileSync(path.join(LESSONS_DIR, f), 'utf8');
  const m = md.match(/##\s*Sources\s*\n([\s\S]*?)(\n##\s|$)/i);
  if (!m) continue;
  const lessonTitle = (md.match(/^#\s+(.+)$/m) || [, f])[1].trim();
  for (const line of m[1].split('\n')) {
    const t = line.replace(/^\s*[-*]\s+/, '').trim();
    if (t.length < 12) continue;
    if (/primary sources finalized in citation review/i.test(t)) continue;
    const doiM = t.match(doiRe);
    const doi = doiM ? doiM[0].replace(/[).,]+$/, '').toLowerCase() : '';
    const key = doi || t.toLowerCase().slice(0, 80);
    if (!sources.has(key)) sources.set(key, { text: t, doi, lessons: new Set() });
    sources.get(key).lessons.add(lessonTitle);
    if (!doi) noDoi.push({ text: t, lesson: lessonTitle });
  }
}

// classify + Crossref-verify non-corpus DOIs
const rows = [];
let inCorpus = 0, crossrefOk = 0, unverified = 0, noDoiCount = 0;
const toCheck = [...sources.values()].filter((s) => s.doi && !corpusDoi.has(s.doi));
const checked = new Map();
for (const s of toCheck) {
  try {
    const r = await fetch(`https://api.crossref.org/works/${encodeURIComponent(s.doi)}?mailto=${MAILTO}`, { headers: { 'User-Agent': `ces-citation-audit (mailto:${MAILTO})` } });
    checked.set(s.doi, r.status === 200);
  } catch { checked.set(s.doi, false); }
  await sleep(400); // throttle < 3/s
}

for (const s of sources.values()) {
  let status;
  if (!s.doi) { status = 'no-doi'; noDoiCount++; }
  else if (corpusDoi.has(s.doi)) { status = 'corpus'; inCorpus++; }
  else if (checked.get(s.doi)) { status = 'crossref-ok'; crossrefOk++; }
  else { status = 'UNVERIFIED'; unverified++; }
  rows.push({ ...s, status });
}

// master source + usage map
const map = ['# Master source and usage map (NASM CES)', '',
  `Generated by scripts/ces-citation-audit.mjs. ${sources.size} unique sources across the lesson Sources sections.`,
  `Status: corpus = in the shared open-access bibliography; crossref-ok = DOI verified live at Crossref; UNVERIFIED = DOI did not resolve (treat as suspect); no-doi = needs a DOI.`, ''];
for (const r of rows.sort((a, b) => a.text.localeCompare(b.text))) {
  map.push(`- [${r.status}] ${r.text}`);
  map.push(`  - cited in: ${[...r.lessons].join('; ')}`);
}
fs.writeFileSync(`${TEACHER}/master-source-usage-map.md`, map.join('\n') + '\n');

// sources needed (no-doi)
const needed = ['# Sources still needed (NASM CES)', '',
  'Citations with no DOI. Confirm a real primary source and add its DOI, or restate the claim generally with no citation.', ''];
for (const n of noDoi) needed.push(`- ${n.text}  _(in: ${n.lesson})_`);
fs.writeFileSync(`${TEACHER}/sources-needed.md`, needed.join('\n') + '\n');

// fake-claims audit (UNVERIFIED dois)
const fake = ['# Fake / unverified claims audit (NASM CES)', '',
  'DOIs that did NOT resolve at Crossref. Each must be corrected to a real source or cut before publishing (CitationIntegrityGuide).', ''];
const bad = rows.filter((r) => r.status === 'UNVERIFIED');
for (const r of bad) { fake.push(`- ${r.text}`); fake.push(`  - cited in: ${[...r.lessons].join('; ')}`); }
if (!bad.length) fake.push('None: every DOI is either in the corpus or resolved at Crossref.');
fs.writeFileSync(`${TEACHER}/fake-claims-audit.md`, fake.join('\n') + '\n');

console.log(JSON.stringify({ uniqueSources: sources.size, inCorpus, crossrefOk, unverified, noDoiCitations: noDoi.length, artifacts: `${TEACHER}/` }, null, 2));
